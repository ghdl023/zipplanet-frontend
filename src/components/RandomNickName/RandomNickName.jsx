function RandomNickName(props) {

    const onClickRandom = ()=>{
        let guIdx = Math.floor(Math.random() * guList.length);
        let dongIdx = Math.floor(Math.random() * list[guList[guIdx]].length);
        let adjIdx = Math.floor(Math.random() * adjectives.length);
        let targetIdx = Math.floor(Math.random() * target.length);
        let dong = list[guList[guIdx]][dongIdx];
        let adj = adjectives[adjIdx];
        let tar = target[targetIdx];
        
        props.setNewNickname(dong+adj+tar);
    }

    return (
        <button onClick={onClickRandom}>랜덤추천</button>
    );
}
